function MovieList({ movies }) {
  // const [movies, setMovies] = useState(tempMovieData); // we need this state to
  //                               dynamically calculate search results found for displaying
  //                               in NumResults component and the state lives here in this
  //                               MovieList component so we lift this movies state up from
  //                               here to the common parent component which is App component
  //                               and from there pass the movies state as prop to the Main
  //                               component and then to the ListBox component and then to
  //                               this MovieList component and from the App component we
  //                               also send the movies state down to Navbar component and
  //                               then to NumResults component inside which we dynamically
  //                               display the Number of Search results by doing
  //                               movies.length through prop drilling. This prop drilling
  //                               can be tedious if components are very deeply nested then
  //                               we have to pass the prop through a lot of components. A
  //                               solution of this problem of prop drilling is Component
  //                               Composition. Component composition is combining
  //                               different components using the children prop or
  //                               explicitly defined props. Ex as just below:
  //
  //                               function Modal({ children }){
  //                                 return (
  //                                   <div className="modal">
  //                                     {children}
  //                                   >/div>
  //                                 )
  //                               }
  //
  //                               function Error() {
  //                                 return <p>This went wrong!</p>
  //                               }
  //
  //                               Now with Component composition we can simply do:
  //                               <Modal>
  //                                 <Error />
  //                               </Modal>
  //
  //                               We can use component composition:
  //                               1. When we want to create highly reusable and
  //                                  flexible components (such as the modal window)
  //                               2. To fix prop drilling problem (great for layouts)
  //
  //                               Now we use component composition to fix component
  //                               drilling problem where we pass movies prop into the
  //                               NavBar component and then from there into the
  //                               NumResults component. Here we only have one leve of prop
  //                               drilling. If we could use NumResults component right in
  //                               the App component instead of the NavBar component, then
  //                               we wouldn't have to pass the movies prop into the NavBar
  //                               which will fix that prop drilling problem. So we do this
  //                               with component composition as above. So we pass children
  //                               prop in the NavBar component function and inside it, write
  //                               {children} in the returned <nav></nav> element after the
  //                               <Logo/> component and in the App component, instead of
  //                               just <Navbar/> we write it like an HTML element and inside
  //                               it write <Search/> and <NumResults/> as:
  //
  //                               <NavBar>
  //                                 <Search />
  //                                 <NumResults movies={movies} />
  //                               </NavBar>
  //
  //                               Similarly we do for <Main/> component and for any further
  //                               component if needed
  return (
    <ul className="list">
      {movies?.map((movie) => (
        <Movie movie={movie} key={movie.imdbID} />
      ))}
    </ul>
  );
}

